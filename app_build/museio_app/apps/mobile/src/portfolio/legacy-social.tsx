export function normalizeSocialLinkValue(platform: SocialPlatform, value: string) {
  const trimmed = value.trim();

  if (!trimmed) {
    return "";
  }

  if (platform === "email" && !trimmed.startsWith("mailto:")) {
    return `mailto:${trimmed}`;
  }

  return trimmed;
}

export function displaySocialLinkValue(platform: SocialPlatform, value?: string) {
  if (!value) {
    return "";
  }

  if (platform === "email") {
    return value.replace(/^mailto:/i, "");
  }

  return value;
}

export function visibleSocialEntries(socialLinks: SocialLinks) {
  return legacySocialPlatformOrder
    .map((platform) => [platform, socialLinks[platform]] as const)
    .filter((entry) => Boolean(entry[1]?.trim()));
}

